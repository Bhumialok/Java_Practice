package com.practice_operators;

public class Assignment {

	public static void main(String[] args) {
		int a=5;
		float b=6.7f;
		
		System.out.println("a = "+a);
		System.out.println("b = "+b);
		
		a += b; //adding and assigning values
		
		System.out.println("a = "+b); //displaying assigned values
		
		//can perform several operations like ASSIGN(=), ADD & ASSIGN(+=), SUB & ASSIGN(-=), MUL & ASSIGN(*=), DIV & ASSIGN(/=).
	}

}
